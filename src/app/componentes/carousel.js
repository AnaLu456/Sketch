"use client";

import { useState } from "react";
import { motion, AnimatePresence } from "framer-motion";
import styles from './carousel.module.css';

const Carousel = ({ images }) => {
  console.log(images)
  const [currentIndex, setCurrentIndex] = useState(0);
  const [direction, setDirection] = useState(null);

  const slideVariants = {
    hiddenRight: {
      x: "100%",
      opacity: 0,
    },
    hiddenLeft: {
      x: "-100%",
      opacity: 0,
    },
    visible: {
      x: "0",
      opacity: 1,
      transition: {
        duration: 1,
      },
    },
    exit: {
      opacity: 0,
      scale: 0.8,
      transition: {
        duration: 0.5,
      },
    },
  };
  const slidersVariants = {
    hover: {
      scale: 1.2,
      backgroundColor: "#ff00008e",
    },
  };
  const dotsVariants = {
    initial: {
      y: 0,
    },
    animate: {
      y: -10,
      scale: 1.2,
      transition: { type: "spring", stiffness: 1000, damping: "10" },
    },
    hover: {
      scale: 1.1,
      transition: { duration: 0.2 },
    },
  };

  const handleNext = () => {
    setDirection("right");
    setCurrentIndex((prevIndex) =>
      prevIndex + 1 === images.length ? 0 : prevIndex + 1
    );
  };

  const handlePrevious = () => {
    setDirection("left");

    setCurrentIndex((prevIndex) =>
      prevIndex - 1 < 0 ? images.length - 1 : prevIndex - 1
    );
  };

  const handleDotClick = (index) => {
    setDirection(index > currentIndex ? "right" : "left");
    setCurrentIndex(index);
  };

  const imageContainer = (
    <div className={styles.carouselImg}>
      <img src={images[currentIndex].src} alt="Image" />
    </div>
  );

  const titleContainer = (
    <div className={styles.titleContainer}>
      <h1>{images[currentIndex].title}</h1>
      <button>Saber Mas</button>
    </div>
  );

  return (
    <div className={styles.carouselImages}>

      <div className={styles.carouselImg}>
        <AnimatePresence>
          {imageContainer}
          <div className={styles.titleContainer}>
            <h1 >{images[currentIndex].title}</h1>
          </div>
        </AnimatePresence>
        <div className={styles.slidedirection}>
          <motion.div
            variants={slidersVariants}
            whileHover="hover"
            className={styles.left}
            onClick={handlePrevious}
          >
            <svg className={styles.flechaizsvg}
              xmlns="http://www.w3.org/2000/svg"
              height="15"
              viewBox="0 96 960 960"
              width="15"
            >
              <path d="M400 976 0 576l400-400 56 57-343 343 343 343-56 57Z" />
            </svg>
          </motion.div>
          <motion.div
            variants={slidersVariants}
            whileHover="hover"
            className={styles.right}
            onClick={handleNext}
          >
            <svg className={styles.flechasvg}
              xmlns="http://www.w3.org/2000/svg"
              height="15"
              viewBox="0 96 960 960"
              width="15"

            >
              <path d="m304 974-56-57 343-343-343-343 56-57 400 400-400 400Z" />
            </svg>
          </motion.div>
        </div>
      
      <div className={styles.dotspace}>
        {images.map((_, index) => (
          <motion.div
            key={index}
            className={`${styles.dot} ${currentIndex === index ? "active" : ""}`}
            onClick={() => handleDotClick(index)}
            initial="initial"
            animate={currentIndex === index ? "animate" : ""}
            whileHover="hover"
            variants={dotsVariants}
          ></motion.div>
        ))}
      </div>
    </div>
  </div>
  );
};
export default Carousel;